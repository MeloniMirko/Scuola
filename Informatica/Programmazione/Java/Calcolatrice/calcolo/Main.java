package calcolo;

public class Main {
    public static void main(String[] args) {
        int num1 = 5; // Puoi cambiare questi valori di prova
        int num2 = 3;

        int somma = num1 + num2;
        int differenza = num1 - num2;
        int prodotto = num1 * num2;
        int quoziente = num1 / num2;

        System.out.println("La somma di " + num1 + " e " + num2 + " è: " + somma);
        System.out.println("La differenza tra " + num1 + " e " + num2 + " è: " + differenza);
        System.out.println("Il prodotto di " + num1 + " e " + num2 + " è: " + prodotto);
        System.out.println("Il quoziente di " + num1 + " diviso " + num2 + " è: " + quoziente);
    }
}
